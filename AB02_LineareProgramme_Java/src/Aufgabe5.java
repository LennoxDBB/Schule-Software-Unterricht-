
public class Aufgabe5 {

	public static void main(String[] args) {
		
		int a = 12;
		int b = 3;
		
		System.out.println(a + " + " + b + " = " + String.valueOf(a + b));
		System.out.println(a + " - " + b + " = " + String.valueOf(a - b));
		System.out.println(a + " * " + b + " = " + a * b);
		System.out.print(a + " / " + b + " = " + a / b);
		
	}

}
